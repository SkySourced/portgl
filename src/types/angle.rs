/// Represents an angle.
pub struct Angle {
    pub rad: f32,
}

impl Angle {
    /// Creates an Angle from radians
    pub fn from_rad(rad: f32) -> Angle {
        Angle { rad: rad }
    }

    /// Creates an Angle from degrees
    pub fn from_deg(deg: f32) -> Angle {
        Angle {
            rad: deg / 180.0 * PI,
        }
    }

    /// Returns the sine of the angle.
    pub fn sin(&self) -> f32 {
        sin(self.rad)
    }

    /// Returns the cosine of the angle. 
    pub fn cos(&self) -> f32 {
        cos(self.rad)
    }
}

/// Returns the sine of a given angle in radians.
/// The LUT used has a resolution to the thousands
/// place, then lerps between values.
pub fn sin(mut rad: f32) -> f32 {
    let negative = rad < 0.0;
    if negative {
        rad *= -1.0;
    }
    rad = rad % (PI * 2.0); // restrict to range of sin

    let sine: f32;
    let n1: f32;
    let n2: f32;

    let index = ((rad % (PI / 2.0)) * 1000.0) as usize;
    let inv_index = 1570 - index;

    if rad == 0.0 {
        return 0.0;
    } else if rad < PI / 2.0 {
        n1 = SINES[index];
        if index == 1571 {
            n2 = 1.0;
        } else {
            n2 = SINES[index + 1];
        }

    } else if rad == PI / 2.0 {
        return 1.0;
    } else if rad < PI {
        n2 = SINES[inv_index];
        if index == 1571 {
            n1 = 0.0;
        } else {
            n1 = SINES[inv_index - 1];
        }
    } else if rad == PI {
        return 0.0;
    } else if rad < PI * 3.0 / 2.0 {
        n1 = SINES[index] * -1.0;
        if index == 1571 {
            n2 = -1.0;
        } else {
            n2 = SINES[index + 1] * -1.0;
        }
    } else if rad == PI * 3.0 / 2.0 {
        return -1.0;
    } else {
        n2 = SINES[inv_index] * -1.0;
        if index == 1571 {
            n1 = 0.0;
        } else {
            n1 = SINES[inv_index - 1] * -1.0;
        }
    }

    sine = n1 + (n2 - n1) * (rad - ((rad as i32) / 1000) as f32);

    if negative {
        sine * -1.0
    } else {
        sine
    }
}

/// Returns the sine of a given angle in radians.
/// Uses angle::sin and rotates it by pi/2.
pub fn cos(rad: f32) -> f32 {
    sin(rad + (PI / 2.0))
}
